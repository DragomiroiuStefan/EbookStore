CREATE TABLE BOOK_TYPES (
    "TYPE" VARCHAR(25) NOT NULL, 
    PRIMARY KEY ("TYPE")
);
CREATE TABLE BOOK_PAPER_QUALITIES (
    QUALITY VARCHAR(25) NOT NULL, 
    PRIMARY KEY (QUALITY)
);
CREATE TABLE BOOK_GENRES (
    GENRE VARCHAR(25) NOT NULL, 
    PRIMARY KEY (GENRE)
);
CREATE TABLE EBOOKS (
    ISBN VARCHAR(50) NOT NULL, 
    "NAME" VARCHAR(50) NOT NULL, 
    ID_TYPE VARCHAR(25) NOT NULL REFERENCES BOOK_TYPES("TYPE"), 
    ID_QUALITY VARCHAR(25) NOT NULL REFERENCES BOOK_PAPER_QUALITIES(QUALITY), 
    PAGES INTEGER NOT NULL, 
    ID_GENRE VARCHAR(25) NOT NULL REFERENCES BOOK_GENRES(GENRE), 
    PRIMARY KEY (ISBN)
);
CREATE TABLE BOOK_AUTHORS (
    SSN VARCHAR(13) NOT NULL, 
    FIRST_NAME VARCHAR(30) NOT NULL, 
    FAMILY_NAME VARCHAR(30) NOT NULL, 
    PRIMARY KEY (SSN)
);
CREATE TABLE EBOOKS_AUTHORS (
    EBOOK_ID INTEGER NOT NULL, 
    ID_ISBN VARCHAR(50) NOT NULL REFERENCES EBOOKS(ISBN), 
    ID_SSN VARCHAR(13) NOT NULL REFERENCES BOOK_AUTHORS(SSN), 
    PRIMARY KEY (EBOOK_ID)
);
CREATE TABLE RATINGS (
    RATING VARCHAR(5) NOT NULL,
    PRIMARY KEY (RATING)
);
CREATE TABLE ROLES (
    "ROLE" VARCHAR(25) NOT NULL, 
    PRIMARY KEY ("ROLE")
);
CREATE TABLE USERS (
    SSN VARCHAR(13) NOT NULL, 
    USERNAME VARCHAR(25) NOT NULL, 
    PASSWORD VARCHAR(25) NOT NULL,
    "ROLE" VARCHAR(25) NOT NULL REFERENCES ROLES("ROLE"), 
    PRIMARY KEY (SSN)
);
CREATE TABLE EBOOKS_RATINGS_USERS (
    ID INTEGER NOT NULL, 
    ID_RATING VARCHAR(5) NOT NULL REFERENCES RATINGS(RATING), 
    ID_SSN VARCHAR(13) NOT NULL REFERENCES USERS(SSN),
    ID_ISBN VARCHAR(50) NOT NULL REFERENCES EBOOKS(ISBN), 
    PRIMARY KEY (ID)
);

